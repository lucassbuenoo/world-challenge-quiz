export function normalizeCountryName(name: string): string {
  return name.toLowerCase()
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '') // Remove acentos
    .trim();
}

export function findCountryByName(inputName: string): Country | undefined {
  const normalizedInput = normalizeCountryName(inputName);
  
  return countries.find(country => {
    const normalizedCountryName = normalizeCountryName(country.name);
    const normalizedAlternatives = country.alternativeNames.map(alt => normalizeCountryName(alt));
    
    return normalizedCountryName === normalizedInput || 
           normalizedAlternatives.includes(normalizedInput);
  });
}